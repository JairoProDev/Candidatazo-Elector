type Section = { title: string; id: string; content: string };

function slugify(input: string) {
  return input
    .toLowerCase()
    .normalize("NFD")
    .replace(/\p{Diacritic}/gu, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "");
}

function parseSections(raw: string): Section[] {
  const lines = raw.split("\n");
  const sections: Section[] = [];

  let activeTitle: string | null = null;
  let activeLines: string[] = [];

  const isHeadingLine = (trimmed: string) => (SECTION_TITLES as readonly string[]).includes(trimmed);

  const flush = () => {
    if (!activeTitle) return;
    const content = activeLines.join("\n").trim();
    sections.push({
      title: activeTitle,
      id: slugify(activeTitle),
      content,
    });
  };

  for (const line of lines) {
    const trimmed = line.trim();
    if (isHeadingLine(trimmed)) {
      flush();
      activeTitle = trimmed;
      activeLines = [];
      continue;
    }

    if (activeTitle) activeLines.push(line);
  }

  flush();
  return sections;
}

function formatTextWithTables(text: string) {
  // UX-friendly formatting: group consecutive "a\tb\tc" rows into an HTML table.
  const lines = text.split("\n");
  const rendered: React.ReactNode[] = [];

  let i = 0;
  while (i < lines.length) {
    const line = lines[i];

    if (line.includes("\t") && line.trim().length > 0) {
      const tableLines: string[] = [];
      while (i < lines.length && lines[i].includes("\t") && lines[i].trim().length > 0) {
        tableLines.push(lines[i]);
        i += 1;
      }

      const rows = tableLines.map((l) => l.split("\t"));
      const colCount = Math.max(...rows.map((r) => r.length));
      const header = rows[0];
      const body = rows.slice(1);

      rendered.push(
        <div key={i} className="my-4 overflow-x-auto">
          <table className="w-full text-sm text-gray-700 border border-gray-200 rounded-lg overflow-hidden">
            <thead className="bg-gray-100">
              <tr>
                {Array.from({ length: colCount }).map((_, colIdx) => (
                  <th
                    key={colIdx}
                    className="text-left px-3 py-2 font-semibold text-gray-800 border-b border-gray-200 whitespace-pre-line"
                  >
                    {header[colIdx] ?? ""}
                  </th>
                ))}
              </tr>
            </thead>
            <tbody className="bg-white">
              {body.map((row, rowIdx) => (
                <tr key={rowIdx} className={rowIdx % 2 === 0 ? "bg-white" : "bg-gray-50"}>
                  {Array.from({ length: colCount }).map((_, colIdx) => (
                    <td
                      key={colIdx}
                      className="px-3 py-2 border-b border-gray-100 whitespace-pre-line"
                    >
                      {row[colIdx] ?? ""}
                    </td>
                  ))}
                </tr>
              ))}
            </tbody>
          </table>
        </div>,
      );

      continue;
    }

    rendered.push(
      <div
        key={i}
        className={line.trim().length === 0 ? "h-2" : "whitespace-pre-line text-gray-700 leading-relaxed"}
      >
        {line}
      </div>,
    );
    i += 1;
  }

  return rendered;
}

